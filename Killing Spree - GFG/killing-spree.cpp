class Solution {
public:
    long long int killinSpree(long long int n)
    {
          int count = 0;
        for(int i = 1;n > 0;i++){
            n = n - (i * i);
            count++;
            if(n < 0)
                count--;
        }
        return count;// Code Here
    }
};


